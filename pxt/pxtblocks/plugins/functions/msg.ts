defineMsg(MsgKey.FUNCTIONS_DEFAULT_NUMBER_ARG_NAME, "num");

defineMsg(MsgKey.FUNCTIONS_DEFAULT_ARRAY_ARG_NAME, "array");

defineMsg(MsgKey.FUNCTIONS_DEFAULT_CUSTOM_ARG_NAME, "value");

defineMsg(MsgKey.FUNCTIONS_DEFAULT_FUNCTION_NAME, "doSomething");

defineMsg(MsgKey.FUNCTION_CREATE_NEW, "Make a Function");

defineMsg(MsgKey.FUNCTION_FLYOUT_LABEL, "Your Functions");

defineMsg(MsgKey.FUNCTIONS_EDIT_OPTION, "Edit Function");

defineMsg(MsgKey.FUNCTIONS_CREATE_CALL_OPTION, "Create 'call %1'");